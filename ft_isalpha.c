#include "libft.h"

/** 
 * @brief  Test if [c] is an alphabetical character
 * @param  int c
 * @retval int
 */
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
